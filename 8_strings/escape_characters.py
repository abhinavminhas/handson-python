# To insert characters that are illegal in a string, use an escape character.

txt = "We are the so-called \"Vikings\" from the north."
print(txt)

print()
txt = 'We are the so-called \'Vikings\' from the north.'
print(txt)

# Escape Characters
# \'	Single Quote	
# \\	Backslash	
# \n	New Line	
# \r	Carriage Return	
# \t	Tab	
# \b	Backspace	
# \f	Form Feed	
# \ooo	Octal value	
# \xhh	Hex value